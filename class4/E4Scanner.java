package Java.class4;

import java.util.Scanner;

public class E4Scanner {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int[] arr = new int[sc.nextInt()];
        for(int i = 0; i <arr.length; i--){
            System.out.println(arr[i]);
            System.out.println();

        }
    }
}
